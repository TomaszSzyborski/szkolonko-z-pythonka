"""
Porównania
"""
print("Czy 5 jest większe od 4?:")
print(5 > 4)

print("Czy 5 jest mniejsze od 4?:")
print(5 < 4)

print("Czy 5 jest równe od 4?:")
print(5 == 4)

print("Czy 5 jest różne od 4?:")
print(5 != 4)


"""
Łączenie operatorów logiki i porównainia
"""
print(3 < 2 and not 4 == 3 or 2 != 0)
print(3 < 2 and not (4 == 3 or 2 != 0))
